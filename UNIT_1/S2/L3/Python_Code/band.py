# Programma per generare un nome di una band

# Chiedi all'utente il nome della città
citta = input("Inserisci il nome della tua città di origine: ")

# Chiedi all'utente il nome del proprio animale domestico
animale = input("Inserisci il nome del tuo animale domestico: ")

# Genera il nome della band combinando i due input
nome_band = citta + " " + animale

# Stampa il risultato
print("Il nome della tua band potrebbe essere:", nome_band)
